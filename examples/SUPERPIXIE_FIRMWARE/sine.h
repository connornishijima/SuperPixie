int16_t get_sine(uint16_t index_10_bit){
  uint8_t  reverse_bit  = bitRead(index_10_bit, 8);
  uint8_t  negative_bit = bitRead(index_10_bit, 9);
  uint16_t index_mod = index_10_bit % 256;

  int16_t output = 0;

  if(!reverse_bit){
    output = sine[index_mod];
  }
  else{
    output = sine[255-index_mod];
  }

  if(negative_bit){
    output *= -1;
  }

  return output;
}

int16_t get_sine_float_index(float index){
  return get_sine(1023*fmod(index, 1.0));
}